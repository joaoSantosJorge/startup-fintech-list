def fetch_all_data(conn):
    rows = conn.execute("""
        SELECT c.id, c.name, c.one_liner, c.long_description, c.website,
               c.status, c.batch, c.stage, c.team_size, c.subindustry,
               e.urgency, e.market_size, e.pricing_potential, e.acquisition_cost,
               e.delivery_cost, e.uniqueness, e.speed_to_market, e.upfront_investment,
               e.upsell_potential, e.evergreen_potential, e.total_score,
               e.replicability, e.niche_category, e.reasoning
        FROM evaluations e
        JOIN companies c ON e.company_id = c.id
        ORDER BY e.total_score DESC
    """).fetchall()
    return [dict(r) for r in rows]


def score_badge_class(score):
    if score >= 70:
        return "bg-emerald-100 text-emerald-800"
    elif score >= 55:
        return "bg-blue-100 text-blue-800"
    elif score >= 40:
        return "bg-amber-100 text-amber-800"
    return "bg-red-100 text-red-800"


