def compute_recommendations(companies):
    # Most replicable (replicability >= 7, top 20 by score)
    replicable = sorted(
        [c for c in companies if c["replicability"] >= 7],
        key=lambda c: c["total_score"], reverse=True
    )[:20]

    # Niche opportunity scores
    niche_data = {}
    for c in companies:
        n = c["niche_category"]
        if n not in niche_data:
            niche_data[n] = {"scores": [], "replicabilities": [], "dims": {d: [] for d in DIMENSIONS}}
        niche_data[n]["scores"].append(c["total_score"])
        niche_data[n]["replicabilities"].append(c["replicability"])
        for d in DIMENSIONS:
            niche_data[n]["dims"][d].append(c[d])

    niche_opp = []
    for n, nd in niche_data.items():
        avg_score = sum(nd["scores"]) / len(nd["scores"])
        avg_replic = sum(nd["replicabilities"]) / len(nd["replicabilities"])
        opp_score = round(avg_score * 0.5 + avg_replic * 3 + max(0, 10 - len(nd["scores"]) * 0.3) * 2, 1)
        niche_opp.append({
            "niche": n, "count": len(nd["scores"]),
            "avg_score": round(avg_score, 1),
            "avg_replic": round(avg_replic, 1),
            "opp_score": opp_score,
        })
    niche_opp.sort(key=lambda x: x["opp_score"], reverse=True)

    # Strategic insights
    all_scores = [c["total_score"] for c in companies]

    # Highest-scoring dimension
    dim_avgs = {d: sum(c[d] for c in companies) / len(companies) for d in DIMENSIONS}
    best_dim = max(dim_avgs, key=dim_avgs.get)

    # Most variance dimension
    dim_vars = {d: statistics.stdev(c[d] for c in companies) for d in DIMENSIONS}
    most_var_dim = max(dim_vars, key=dim_vars.get)

    # Sweet spot companies
    sweet_spot = [c for c in companies if c["total_score"] >= 65 and c["replicability"] >= 6]

    # Underserved niche
    avg_global = sum(all_scores) / len(all_scores)
    above_avg_niches = [no for no in niche_opp if no["avg_score"] >= avg_global]
    underserved = min(above_avg_niches, key=lambda x: x["count"]) if above_avg_niches else None

    # Active vs Dead
    active = [c for c in companies if c["status"] == "Active"]
    inactive = [c for c in companies if c["status"] != "Active"]
    active_avg = round(sum(c["total_score"] for c in active) / len(active), 1) if active else 0
    inactive_avg = round(sum(c["total_score"] for c in inactive) / len(inactive), 1) if inactive else 0

    # Quick wins
    quick_wins = [c for c in companies if c["speed_to_market"] >= 8 and c["upfront_investment"] >= 8]
    quick_wins.sort(key=lambda c: c["total_score"], reverse=True)

    return {
        "replicable": [{
            "name": c["name"], "one_liner": c["one_liner"] or "", "score": c["total_score"],
            "replicability": c["replicability"], "niche": c["niche_category"], "reasoning": c["reasoning"] or "",
        } for c in replicable],
        "niche_opp": niche_opp,
        "insights": {
            "best_dim": {"dim": best_dim.replace("_", " ").title(), "avg": round(dim_avgs[best_dim], 1)},
            "most_var_dim": {"dim": most_var_dim.replace("_", " ").title(), "stdev": round(dim_vars[most_var_dim], 2)},
            "sweet_spot_count": len(sweet_spot),
            "underserved": underserved,
            "active_avg": active_avg,
            "inactive_avg": inactive_avg,
            "active_count": len(active),
            "inactive_count": len(inactive),
            "quick_wins_count": len(quick_wins),
            "quick_wins_top3": [{"name": c["name"], "score": c["total_score"]} for c in quick_wins[:3]],
        }
    }


