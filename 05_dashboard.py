def compute_analysis_data(companies):
    top20 = companies[:20]

    # Score distribution
    buckets = {"Excellent (70+)": 0, "Strong (55-69)": 0, "Average (40-54)": 0, "Weak (<40)": 0}
    for c in companies:
        s = c["total_score"]
        if s >= 70: buckets["Excellent (70+)"] += 1
        elif s >= 55: buckets["Strong (55-69)"] += 1
        elif s >= 40: buckets["Average (40-54)"] += 1
        else: buckets["Weak (<40)"] += 1

    # Best niches
    niche_data = {}
    for c in companies:
        n = c["niche_category"]
        if n not in niche_data:
            niche_data[n] = {"scores": [], "dims": {d: [] for d in DIMENSIONS}}
        niche_data[n]["scores"].append(c["total_score"])
        for d in DIMENSIONS:
            niche_data[n]["dims"][d].append(c[d])

    niche_stats = []
    for n, nd in niche_data.items():
        niche_stats.append({
            "niche": n,
            "count": len(nd["scores"]),
            "avg_score": round(sum(nd["scores"]) / len(nd["scores"]), 1),
            "dim_avgs": {d: round(sum(nd["dims"][d]) / len(nd["dims"][d]), 1) for d in DIMENSIONS},
        })
    niche_stats.sort(key=lambda x: x["avg_score"], reverse=True)

    # Global dimension averages
    global_dims = {d: round(sum(c[d] for c in companies) / len(companies), 1) for d in DIMENSIONS}

    # Active vs Inactive
    status_data = {}
    for c in companies:
        st = c["status"] or "Unknown"
        if st not in status_data:
            status_data[st] = {"count": 0, "scores": []}
        status_data[st]["count"] += 1
        status_data[st]["scores"].append(c["total_score"])
    status_stats = [{"status": k, "count": v["count"], "avg_score": round(sum(v["scores"]) / len(v["scores"]), 1)} for k, v in status_data.items()]
    status_stats.sort(key=lambda x: x["avg_score"], reverse=True)

    return {
        "top20": [{"name": c["name"], "score": c["total_score"], "niche": c["niche_category"]} for c in top20],
        "buckets": buckets,
        "niche_stats": niche_stats,
        "global_dims": global_dims,
        "status_stats": status_stats,
    }


